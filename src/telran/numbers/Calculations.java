package telran.numbers;

public class Calculations {
     public static double sum(double op1, double op2) {
    	 return op1 + op2;
     }
     public static double subtract(double op1, double op2) {
    	 return op1 - op2;
     }
     public static double multiplay(double op1, double op2) {
    	 return op1 + op2;
     }
     public static double divede(double op1, double op2) {
    	 return op1 / op2;
     }
}
